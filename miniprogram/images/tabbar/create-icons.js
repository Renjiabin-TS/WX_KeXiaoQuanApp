// TabBar 图标生成脚本
// 运行: node create-icons.js

const fs = require('fs');
const path = require('path');

// 创建最小化的 1x1 透明 PNG 作为占位
const transparentPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
  0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, // IDAT chunk
  0x54, 0x08, 0x1D, 0x01, 0x00, 0x00, 0xFF, 0xFF,
  0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0x00, 0x00, // IEND chunk
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44
]);

const tabbarDir = path.join(__dirname);

const icons = [
  'home.png',
  'home-active.png',
  'calendar.png',
  'calendar-active.png',
  'profile.png',
  'profile-active.png'
];

icons.forEach(icon => {
  const iconPath = path.join(tabbarDir, icon);
  if (!fs.existsSync(iconPath)) {
    fs.writeFileSync(iconPath, transparentPng);
    console.log(`Created: ${icon}`);
  } else {
    console.log(`Exists: ${icon}`);
  }
});

console.log('\nTabBar icons ready!');
console.log('Note: These are 1x1 transparent PNG placeholders.');
console.log('Replace them with proper icons later.');
